// Math.floor(); => Zaokrouhluje vždy dolů
let someFloorNum = Math.floor(6.789);
console.log(someFloorNum); // 6

// Math.ceil(); => Zaokrouhluje vždy nahoru
console.log(Math.ceil(6.789)); // 7

// Math.round(); => Zaokrouhluje
console.log(Math.round(6.789)); // 7

// Math.pow(); => Umocníme číslo
console.log(Math.pow(3, 2)); // 9   

// Math.sqrt(); => Odmocníme číslo
console.log(Math.sqrt(64)); // 8