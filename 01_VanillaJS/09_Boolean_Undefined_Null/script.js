// Boolean => true/false
let isMarried = true;
let isNotMarried = false;
console.log(isMarried); // true
console.log(isNotMarried); // false

// Undefined - pokud je nepřiřazená hodnota
let adress;
console.log(adress); // undefined

// Null - znamená, že není hodnota zadaná (ví se o tom)
// Pokud chceme hodnotu vyprázdnit použijeme null
let firstName = "David";
console.log(firstName); // David
firstName = null;
console.log(firstName); // null